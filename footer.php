  <script>
    (function(){
      const canvas = document.getElementById('c');
      const hero = document.getElementById('hero-sec');
      const ctx = canvas.getContext('2d');
      let W, H, nodes, animId;

      const NODE_COUNT = 38;
      const EDGE_DIST = 130;

      function resize(){
        W = canvas.width  = hero.offsetWidth  || 680;
        H = canvas.height = hero.offsetHeight || 480;
        initNodes();
      }

      function mkNode(){
        return {
          x: Math.random() * W,
          y: Math.random() * H,
          vx: (Math.random()-0.5)*0.35,
          vy: (Math.random()-0.5)*0.35,
          r: Math.random()*1.8+1.2,
          alpha: Math.random()*0.5+0.35
        };
      }

      function initNodes(){
        nodes = Array.from({length: NODE_COUNT}, mkNode);
      }

      function draw(){
        ctx.clearRect(0, 0, W, H);

        for(let i=0;i<nodes.length;i++){
          for(let j=i+1;j<nodes.length;j++){
            const dx = nodes[i].x - nodes[j].x;
            const dy = nodes[i].y - nodes[j].y;
            const d = Math.sqrt(dx*dx+dy*dy);
            if(d < EDGE_DIST){
              const alpha = (1 - d/EDGE_DIST) * 0.50;
              ctx.beginPath();
              ctx.moveTo(nodes[i].x, nodes[i].y);
              ctx.lineTo(nodes[j].x, nodes[j].y);
              ctx.strokeStyle = `rgba(255, 255, 255,${alpha})`;
              ctx.lineWidth = 0.6;
              ctx.stroke();
            }
          }
        }

        for(const n of nodes){
          ctx.beginPath();
          ctx.arc(n.x, n.y, n.r, 0, Math.PI*2);
          ctx.fillStyle = `rgba(100,200,255,${n.alpha})`;
          ctx.fill();
        }
      }

      function step(){
        for(const n of nodes){
          n.x += n.vx; n.y += n.vy;
          if(n.x < 0 || n.x > W) n.vx *= -1;
          if(n.y < 0 || n.y > H) n.vy *= -1;
        }
        draw();
        animId = requestAnimationFrame(step);
      }

      resize();
      step();
      window.addEventListener('resize', resize);
    })();
  </script>
